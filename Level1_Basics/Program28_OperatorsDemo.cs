using System;

namespace CSharpPrograms
{
    public class Program28_OperatorsDemo
    {
        public static void Main()
        {
            // Program 28: Operators demonstration
            int a = 10, b = 3;
            Console.WriteLine($"a + b = {a + b}");
            Console.WriteLine($"a - b = {a - b}");
            Console.WriteLine($"a * b = {a * b}");
            Console.WriteLine($"a / b = {a / b}");
            Console.WriteLine($"a % b = {a % b}");
            Console.WriteLine($"a > b = {a > b}");
            Console.WriteLine($"a == b = {a == b}");
            Console.WriteLine($"(a > 5 && b < 5) = {a > 5 && b < 5}");
        }
    }
}

